// Declaring Variables with data types

let myname = "shubham"; // string
const developer = true; // boolean
const testnull = null; // null
const testundefined = undefined; // undefined

console.log(`The data type of  myname  is => ${typeof myname}`);
console.log(`The data type of  developer  is => ${typeof developer}`);
console.log(`The data type of  testnull  is => ${typeof testnull}`);
// in JavaScript, the data type of null is an object.
console.log(`The data type of  testundefined  is => ${typeof testundefined}`);
